func (g *game) getInitialPlayerPos() {
	var x, y int
	for x, y = 0, 0; g.maze[y][x] != 0; x, y = x+1, y+1 {
	}
	g.playerPos = Pair[int, int]{x, y}
}

func (g *game) Layout(outWidth, outHeight int) (w, h int) { return sw, sh }
func (g *game) Update() error {
	return nil
}
func (g *game) Draw(screen *ebiten.Image) {
	cubeSize := v.Vec{sw / float64(mw), sh / float64(mh), 0}
	for y := range g.maze {
		for x := range g.maze[y] {
			if g.maze[y][x] != 0 {
				ebitenutil.DrawRect(screen, cubeSize.X*float64(x), float64(y)*cubeSize.Y, cubeSize.X, cubeSize.Y, color.RGBA{255, 192, 203, 255})
			}
		}
	}
	ebitenutil.DrawRect(screen, cubeSize.X*float64(g.playerPos.X), cubeSize.Y*float64(g.playerPos.Y), cubeSize.X, cubeSize.Y, color.RGBA{255, 255, 0, 255})
}

func main() {
	ebiten.SetWindowSize(sw, sh)
	g := NewGame()
	if err := ebiten.RunGame(g); err != nil {
		log.Fatal(err)
	}
}
